        public JsonResult Put(Student dep)
        {
            string query = @"
                    update dbo.studentDetails set 
                    RollNumber = '" + dep.RollNumber + @"',
                    StudentName = '"+dep.StudentName + @"',
                    Dept = '" + dep.Dept + @"',
                    Mobile = '" + dep.Mobile + @"'
